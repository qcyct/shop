		<!--加载loadding-->

	</view>
</template>

<script>
	import tuiTabs from "@/components/tui-tabs/tui-tabs";
	import tuiButton from "@/components/button/button";
	import tuiLoadmore from "@/components/loadmore/loadmore";
	import tuiNomore from "@/components/nomore/nomore";
	import tuiListCell from "@/components/list-cell/list-cell";
	import tuiSticky from "@/components/sticky/sticky";
	export default {
		components: {
			tuiTabs,
			tuiButton,
			tuiLoadmore,
			tuiNomore,
			tuiListCell,
			tuiSticky
		},
		data() {
			return {
				tabs: [{
					name: "全部"
				}, {
					name: "待发货"
				}, {
					name: "待收货"
				}, {
					name: "待评价"
				}],
				typeText: {
					unpaid: '待评价',
					back: '待收货',
					unreceived: '待发货'
				},
				currentTab: '',
				pageIndex: 1,
				loadding: false,
				pullUpOn: true,
				scrollTop: 0,
				orderList: [],
				orderListDemo: [
					[],
					[],
					[],
					[],
					[]
				]
			}
		},
		methods: {
			change(e) {
				this.currentTab = e.index
				this.orderList = this.orderListDemo[this.currentTab];
				this.getOrderListcompleted()
			},
			getOrderListcompleted() {
				if (this.orderListDemo[this.currentTab].length > 0) {
					return;
				}
				this.$api.getOrderListcompleted({
					type: this.currentTab
				}, res => {
					if (res.code) {
						this.orderListDemo[this.currentTab].push(...res.data)
					}
				})
			}
		},
		onPageScroll(e) {
			this.scrollTop = e.scrollTop;
		},
		onLoad(option) {
			let tbIndex = parseInt(option.tbIndex) + 1 || 0;
			this.orderList = this.orderListDemo[tbIndex];
			this.currentTab = tbIndex;
			this.getOrderListcompleted()
		}
	}
</script>

